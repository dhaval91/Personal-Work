    private void refreshDrugJTable()
    {
        int rowCount = drugJTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)drugJTable.getModel();
        for(int i=rowCount-1; i>=0; --i)
        {
            model.removeRow(i);
        }

        drugList = manufacturer.getDrugCatalog().getDrugList();
        for(Drug drug: drugList)
        {
            Object row[] = new Object[2];
            row[0] = drug;
            row[1] = drug.getId();

            model.addRow(row);

        }
    }

