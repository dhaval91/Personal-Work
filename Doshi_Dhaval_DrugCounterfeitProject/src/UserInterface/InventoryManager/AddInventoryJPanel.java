        c.setTime(new Date());
        String cdate = sdf.format(date);

        Drug drug = (Drug)drugJTable.getValueAt(selectedIndex, 0);
        inventoryItem = manufacturer.getInventoryCatalog().findItem(drug);
        if(inventoryItem == null)
        {
            inventoryItem = manufacturer.getInventoryCatalog().newInventoryItem();
        }
        inventoryItem.setAvail(quantity);
        drug.setActualPrice(Integer.parseInt(actualPriceJTextField.getText()));
        drug.setCeilingPrice(Integer.parseInt(ceilingPriceJTextField.getText()));
        drug.setFloorPrice(Integer.parseInt(floorPriceJTextField.getText()));
        c.add(Calendar.MONTH, Integer.parseInt(expireJTextField.getText()));
        inventoryItem.setExpiryDate(c.getTime());
        inventoryItem.setManufactureDate(date);
        inventoryItem.setDrug(drug);




