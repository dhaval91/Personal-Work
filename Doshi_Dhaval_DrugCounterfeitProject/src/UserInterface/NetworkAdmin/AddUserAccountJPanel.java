        private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

            if(!Validate.validateTextField(txtUsername, lblUsername,0 ))
            {
                return;
            }
            if(!Validate.validateTextField(txtPassword, lblPassword,0 ))
            {
                return;
            }
            UserAccount ua = enterprise.getUad().newUserAccount();
            ua.setUserName(txtUsername.getText());
            ua.setPassword(txtPassword.getText());
            ua.setE(employee);
            ua.setRole(new EnterpriseAdminRole());
            txtPassword.setText("");
            txtUsername.setText("");

            JOptionPane.showMessageDialog(null, "UserAccount Added");
        }

