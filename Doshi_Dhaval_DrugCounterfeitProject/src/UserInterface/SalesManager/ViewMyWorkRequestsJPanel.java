    private void refreshWorkRequestTable()
    {
        int r = jTable1.getRowCount();
        DefaultTableModel m = (DefaultTableModel)jTable1.getModel();

        for(int  i = r-1;i>=0;i--)
        {
            m.removeRow(i);
        }
        //Organization o = enterprise.getOrganizationDirectory().searchUserOrganization(userAccount);
        for(WorkRequest w : userAccount.getSendWorkQueue().getQueue())
        {
            ShipWorkRequest wr = (ShipWorkRequest) w;
            Object row[] = new Object[4];
            row[0]= wr;
            row[1]= wr.getReceiver();
            row[2]= wr.getStatus();
            row[3] = wr.getResloveDate();


            m.addRow(row);
        }

    }
