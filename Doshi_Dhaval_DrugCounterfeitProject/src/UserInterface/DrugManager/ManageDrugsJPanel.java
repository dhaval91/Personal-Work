    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = directoryTable.getSelectedRow();
        if(selectedRow < 0 || selectedRow > directoryTable.getRowCount() )
        {
            JOptionPane.showMessageDialog(null,"Select a Row");
            return;}
        drug = (Drug)directoryTable.getValueAt(selectedRow, 0);
        ViewDrugsDetailJPanel vpdjp = new ViewDrugsDetailJPanel(userProcessContainer ,drug);
        userProcessContainer.add("ViewDrugDetail",vpdjp);
        CardLayout cardLayout = (CardLayout)userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);
    }//GEN-LAST:event_viewButtonActionPerformed

    private void btnGetLicenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetLicenseActionPerformed

        int selectedRow = directoryTable.getSelectedRow();
        FDA fda = null;
        if(selectedRow < 0 || selectedRow > directoryTable.getRowCount())
        {
            JOptionPane.showMessageDialog(null, "No row selected");
            return;
        }
        for(Enterprise e : network.getEnterpriseDirectory().geteList())
        {
            if(e.getEnterpriseName().equalsIgnoreCase("FDA"))
            {
                fda = (FDA)e;
            }
        }
        if(fda == null)
        {
            JOptionPane.showMessageDialog(null, "FDA not present");
            return;
        }
        Drug selectedDrug = (Drug)directoryTable.getValueAt(selectedRow, 0);
        selectedDrug.setLicense(new License());
        LicenseDrugWorkRequest request = (LicenseDrugWorkRequest)fda.getLicensingOrganization().getWorkQueue().newWorkRequest(type.RequestDrugLicense);
        request.setSender(userAccount);
        request.setDrug(selectedDrug);

        request.setStatus(WorkRequest.status.RequestedLicense);
        userAccount.getSendWorkQueue().addWorkRequest(request);
        JOptionPane.showMessageDialog(null, "Drug License Requested");
        Refresh();
    }//GEN-LAST:event_btnGetLicenseActionPerformed

