    private void Refresh(){
        int rowCount = directoryTable.getRowCount();
        for(int i = rowCount-1; i >= 0; i--)
        {
            ((DefaultTableModel)directoryTable.getModel()).removeRow(i);
        }
        if(!manufacturer.getDrugCatalog().getDrugList().isEmpty()) {
            for (Drug drug : manufacturer.getDrugCatalog().getDrugList())
            {
                Object row [] = new Object[3];
                row[0]= drug;
                row[1]=drug.getId();
                if(drug.getLicense()!=null)
                {
                    if(drug.getLicense().isLicensed())
                    {
                        row[2]= "Licensed";
                    }
                    else
                    {
                        row[2]= "License Requested";
                    }
                }
                else
                {
                    row[2] = "Not Licensed";
                }
                    ((DefaultTableModel)directoryTable.getModel()).addRow(row);
            }
        }
    }
